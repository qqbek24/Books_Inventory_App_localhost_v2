import pymysql
from config import Config


def my_db_connection():
    user_login = Config.DB_USER_LOGIN
    user_pass = Config.DB_USER_PASS
    s_host = Config.S_HOST
    s_db = Config.S_DB

    my_db = pymysql.connect(
        user=user_login,
        passwd=user_pass,
        host=s_host,
        database=s_db
    )
    return my_db
